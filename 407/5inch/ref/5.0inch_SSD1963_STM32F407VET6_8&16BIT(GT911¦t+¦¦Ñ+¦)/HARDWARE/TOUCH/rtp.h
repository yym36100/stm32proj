u8 RTP_Read_XY(u16 *x,u16 *y);					//双方向读取(X+Y)
u8 RTP_Read_XY2(u16 *x,u16 *y);					//带加强滤波的双方向坐标读取
void RTP_Drow_Touch_Point(u16 x,u16 y,u16 color);//画一个坐标校准点
void RTP_Draw_Big_Point(u16 x,u16 y,u16 color);	//画一个大点
